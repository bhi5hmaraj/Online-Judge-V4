    private static void solve() {
        
        
        int V[] = nextIntArray(4);
        
        for(int a = V[2]; a <= 2 * V[2]; a++)
            for(int b = V[1]; b <= 2 * V[1]; b++)
                for(int c = V[0]; c <= 2 * V[0]; c++) 
                    if(a < b && b < c && a >= V[3] && a <= 2 * V[3]) {
                        if(b >= V[3] && b <= 2 * V[3]) continue;
                        if(c >= V[3] && c <= 2 * V[3]) continue;
                        println(c);
                        println(b);
                        println(a);
                        return;
                    }
                
        println(-1);
    }
    
    
    
