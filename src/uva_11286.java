	private static String giveMeHash(int arr[])
	{
		StringBuilder sb = new StringBuilder();
		Arrays.sort(arr);
		for(int a:arr)sb.append(a);
		return sb.toString();
	}
    private static <Key> void frequency(Map<Key , java.lang.Integer> mp , Key k)
    {
    	//Finds frequency of of each element of generic type Key
    	Integer query = mp.get(k);
    	if(query == null)
    		mp.put(k, new Integer(1));
    	else	    	
    		mp.put(k, query + 1);	    	
    }
	private static void solve(FastScanner s1, FastWriter out){

		int t = s1.nextInt();
		while(t != 0)
		{
			HashMap<String,Integer> freq = new HashMap<>();
			while(t-->0)
			{
				frequency(freq, giveMeHash(s1.nextIntArray(5)));
			}
			int max = Integer.MIN_VALUE;
			for (Map.Entry<String,Integer> e : freq.entrySet()) 
			{
				max = Math.max(max, e.getValue());
			}
			out.println(max);
			t = s1.nextInt();
		}

	}

