    private static void solve(FastScanner s1, PrintWriter out){

	int n = s1.nextInt();
	int arr[] = s1.nextIntArray(n);
	int max = Integer.MIN_VALUE;
	for(int i=0;i<n;i++)
	{
	    int left = i-1;
	    int right = i + 1;
	    int cnt = 1;
	    while(left >= 0 && left < n - 1 && arr[left] <= arr[left+1])
	    {
		cnt++;
		left--;
	    }
	    while(right < n && right > 0 && arr[right] <= arr[right-1])
	    {
		cnt++;
		right++;
	    }
	    max = Math.max(max,cnt);
	}
	out.print(max);
    }


    
    /************************ SOLUTION ENDS HERE ************************/
    
    
    
    
    
