	private static void solve(FastScanner s1, PrintWriter out){
		
		BigInteger arr[] = new BigInteger[3];
		arr[0] = new BigInteger(s1.nextLine());
		arr[1] = new BigInteger(s1.nextLine());
		arr[2] = new BigInteger(s1.nextLine());
		
		for(int i=0;i<3;i++){
			for(int j=0;j<3;j++){
				for(int k=0;k<3;k++){
					if(arr[i].add(arr[j]).equals(arr[k])){
						out.println("YES");
						return;
					}
				}
			}
		}
		
		out.println("NO");
	}
	
	
	
