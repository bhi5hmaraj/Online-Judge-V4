	private static void solve(FastScanner s1, PrintWriter out){

		N = s1.nextInt();
		long fact = 1;
		for(int i=1;i<=N;i++)
			fact = MM.mul(fact, i);
		
		long sumOfLeaves = MM.div(fact, 2);
		long ans = MM.mul(sumOfLeaves, N);
		
		out.println(ans);

	}



