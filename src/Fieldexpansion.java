    private static void solve() {
        
        
        long a = nextLong();
        long b = nextLong();
        long h = nextLong();
        long w = nextLong();
        
        int n = nextInt();
        long arr[] = nextLongArray(n);
        Arrays.sort(arr);
        for(int i = 0; i < n >> 1; i++) {
            long temp = arr[i];
            arr[i] = arr[n - i - 1];
            arr[n - i - 1] = temp;
        }
        
        
        
        
    }
    
    
    
