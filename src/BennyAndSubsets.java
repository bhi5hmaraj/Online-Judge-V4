    private static int numberOfSubsets(int arr[],int index,int X,int xor_sum){
	
	if(index == arr.length){
	    return xor_sum == X ? 1 : 0;
	}
	else{
	    return numberOfSubsets(arr, index + 1, X, xor_sum ^ arr[index]) + numberOfSubsets(arr, index + 1, X, xor_sum);
	}
    }
    
